#include "../includes/doop.h"

int	sum(int nb1, int nb2)
{
	return (nb1 + nb2);
}

int	soustraction(int nb1, int nb2)
{
	return (nb1 - nb2);
}

int	multiplication(int nb1, int nb2)
{
	return (nb1 * nb2);
}

int	division(int nb1, int nb2)
{
	return (nb1 / nb2);
}

int	mod(int nb1, int nb2)
{
	return (nb1 % nb2);
}
