#include "../includes/ft.h"

char	*ft_strcpy(char *dst, char *src)
{
	char	*dst_0;

	dst_0 = dst;
	while (*src)
		*dst++ = *src++;
	while (*dst)
		*dst++ = '\0';
	return (dst_0);
}
