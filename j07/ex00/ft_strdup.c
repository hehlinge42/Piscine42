#include <stdlib.h>

char	*ft_strcpy(char *dest, char *src)
{
	unsigned int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int	ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

char *ft_strdup(char *src)
{
	int		size_src;
	char	*cpy;

	size_src = ft_strlen(src);
	cpy = (char*)(malloc(sizeof(*cpy) * size_src));
	ft_strcpy(cpy, src);
	return (cpy);
}
